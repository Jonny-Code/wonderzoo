package wonderzoo;

public class Food {

  private String name;
  private String description;

  public Food(String n) {
    name = n;
  }

  public String getDescription() {
    return description;
  }

  public void setDescription(String d) {
    description = d;
  }

  public String getName() {
    return name;
  }

  public void setName(String n) {
    name = n;
  }
}

